def tr(lang: Lang, script: Script, key: str) -> str:
    if lang == "en":
        return _UI_EN.get(key, key)
    if script == "cyrillic":
        return (
            _UI_YU_CYR.get(key)
            or _UI_YU_LAT.get(key)
            or _UI_EN.get(key, key)
        )
    return _UI_YU_LAT.get(key) or _UI_EN.get(key, key)
